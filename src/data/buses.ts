export function generateSeats(busId: string, price: number): Seat[] {
  const seats: Seat[] = [];
  const lowerSeats = 20;
  const upperSeats = 20;

  const bookedLower = [2, 5, 8, 11, 14, 17];
  const bookedUpper = [1, 4, 7, 10, 13];

  for (let i = 1; i <= lowerSeats; i++) {
    seats.push({
      id: `${busId}-L${i}`,
      number: `L${i}`,
      status: bookedLower.includes(i) ? 'booked' : 'available',
      deck: 'lower',
      isWindow: i % 4 === 1 || i % 4 === 0,
      price,
    });
  }

  for (let i = 1; i <= upperSeats; i++) {
    seats.push({
      id: `${busId}-U${i}`,
      number: `U${i}`,
      status: bookedUpper.includes(i) ? 'booked' : 'available',
      deck: 'upper',
      isWindow: i % 4 === 1 || i % 4 === 0,
      price: price - 50,
    });
  }

  return seats;
}
